import { expect } from 'chai'
import { natsort } from '../lib/index.js'

describe('ip addresses: ', () => {

    it('ipv4', () => {
        expect([
            '192.168.0.100',
            '192.168.0.1',
            '192.168.1.1',
            '192.168.0.250',
            '192.168.1.123',
            '10.0.0.2',
            '10.0.0.1',
        ].sort(natsort())).to.eql([
            '10.0.0.1',
            '10.0.0.2',
            '192.168.0.1',
            '192.168.0.100',
            '192.168.0.250',
            '192.168.1.1',
            '192.168.1.123',
        ])
    })

})